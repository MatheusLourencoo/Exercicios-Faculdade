//Usando o switch , crie um programa que converte dias da semana (de "segunda" a "domingo") em
//números (de 1 a 7).

let diaDaSemana = prompt("Escreva um dia da semana (Segunda a Domingo)");

switch (diaDaSemana) {
    case "Segunda":
        console.log("1");
        break;
    case "Terça":
        console.log("2");
        break;
    case "Quarta":
        console.log("2");
        break;
    case "Quinta":
        console.log("2");
        break;
    case "Sexta":
        console.log("2");
        break;
    case "Sabado":
        console.log("2");
        break;
    case "Domingo":
        console.log("2");
        break;
    default:
        console.log("Inválido");
} 